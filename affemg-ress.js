function semAcento(texto) {
  ACENTOS     = "áÁàÀâÂãÃâäÄéÉèÈêÊëËíÍìÌîÎïÏóÓòÒôÔõÕöÖúÚùÙûÛüÜçÇ";
  SEM_ACENTOS = "aAaAaAaAaaAeEeEeEeEiIiIiIiIoOoOoOoOoOuUuUuUuUcC";
  REGULAR     = " ABCDEFGHIJKLMNOPQRSTUWVXYZabcdefghijklmnopqrstuwvxyz";
  
  result = texto;
  for (i = 0;  i < result.length; i++) {
    c = result.charAt(i);
    if (c.search(REGULAR) < 0) {
      p = ACENTOS.search(c);
      if (p > -1) {
        c = SEM_ACENTOS.charAt(p);
        result = result.slice(0, i) + c + result.slice(i + 1, result.length);
      }
    }
  }
  return result;
}

